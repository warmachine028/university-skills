""" CODE 10:
Write a Python program to check if a given value is present in a set or not. 

Input: [4, 2, 9, 5, 1, 0, 7] 
element -> 5
Output: Present

"""

element = 5
s = {*[4, 2, 9, 5, 1, 0, 7]}
present = element in s
print(f"Output: {'Present' if present else 'Not Present'}")



























